const MAX_ITEMS_PER_SECTION = 10;

function formatUtcTimestamp(timestamp) {
  return new Date(timestamp).toISOString().replace("T", " ").replace(".000Z", " UTC");
}

function formatDaysSince(timestamp, nowTimestamp) {
  const diffMs = Math.max(0, nowTimestamp - new Date(timestamp).getTime());
  const diffDays = Math.floor(diffMs / (24 * 60 * 60 * 1000));
  return `${diffDays}d`;
}

function formatSeverity(severity) {
  return typeof severity === "string" && severity !== "" ? severity.toUpperCase() : "UNKNOWN";
}

function renderList(items, renderItem) {
  if (items.length === 0) {
    return "- None\n";
  }

  return items.slice(0, MAX_ITEMS_PER_SECTION).map(renderItem).join("");
}

export function hasActionableDailyMaintenance({
  pullRequests,
  codeScanningAlerts,
  dependabotAlerts,
  fetchErrors
}) {
  return (
    pullRequests.length > 0 ||
    codeScanningAlerts.length > 0 ||
    dependabotAlerts.length > 0 ||
    fetchErrors.length > 0
  );
}

export function buildDailyMaintenanceBody({
  repository,
  generatedAt,
  pullRequests,
  codeScanningAlerts,
  dependabotAlerts,
  fetchErrors
}) {
  const nowTimestamp = new Date(generatedAt).getTime();
  let body = `# Daily GitHub Maintenance\n\n`;
  body += `Repository: \`${repository}\`\n`;
  body += `Updated: ${formatUtcTimestamp(generatedAt)}\n\n`;

  if (fetchErrors.length > 0) {
    body += `## Fetch Errors (${fetchErrors.length})\n`;
    body += fetchErrors.map((message) => `- ${message}\n`).join("");
    body += "\n";
  }

  body += `## Open Pull Requests (${pullRequests.length})\n`;
  body += renderList(pullRequests, (pullRequest) => {
    const age = formatDaysSince(pullRequest.updatedAt, nowTimestamp);
    return `- [#${pullRequest.number}](${pullRequest.url}) ${pullRequest.title} — @${pullRequest.author} — updated ${age} ago\n`;
  });
  body += "\n";

  body += `## Open Code Scanning Alerts (${codeScanningAlerts.length})\n`;
  body += renderList(codeScanningAlerts, (alert) => {
    return `- [#${alert.number}](${alert.url}) ${alert.title} — ${formatSeverity(alert.severity)} — ${alert.tool}\n`;
  });
  body += "\n";

  body += `## Open Dependabot Alerts (${dependabotAlerts.length})\n`;
  body += renderList(dependabotAlerts, (alert) => {
    return `- [#${alert.number}](${alert.url}) ${alert.packageName} — ${formatSeverity(alert.severity)} — ${alert.ecosystem}\n`;
  });
  body += "\n";

  body += `Generated by \`.github/workflows/daily-maintenance.yml\`.\n`;
  return body;
}
